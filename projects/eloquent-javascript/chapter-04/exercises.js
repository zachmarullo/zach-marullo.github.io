function range(start, end, step) {
  var arr1 = [];
  if (step === undefined) {
    if (start < end) {
      for (let i = start; i <= end; i++) {
        arr1.push(i);
      }
    } else if (start > end) {
      for (let i = end; i >= start; i--){
        arr1.push(i);
      }
    } else if (start === end) {
      return arr1;
    }
    return arr1;
} else if (step !== undefined) {
  if (start < end) {
    for (let k = 0; k <= end; k++) {
      arr1.push(k + step);
    }
  }
}
  return arr1;
};

