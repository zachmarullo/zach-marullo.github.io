////////////////////////////////////////////////////////////////////////////////
// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step) {
  let arr1 = [];
  if (start === end) {
    return arr1;
  }
  if (step === undefined){
    if (start < end) {
      for (let i = start; i <= end; i ++){
        arr1.push(i);
      }
    } else {
      for (let j = start; j >= end; j--) {
        arr1.push(j)
      }
    }
  } else {
    if (step < 0) {
      return arr1;
    }
    if (start < end) {
      for (let i = start; i <= end; i += step){
        arr1.push(i);
      }
    } else {
      for (let j = start; j >= end; j-= step) {
        arr1.push(j)
      }
    }
  }
  return arr1;
}  

 

