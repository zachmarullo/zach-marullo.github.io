function deepEqual(x, y) { 
  if (typeof x !== 'object' && typeof y !== 'object'){
    return x === y;
  } 
  if (typeof x !== 'object' || typeof y !== 'object') {
    return false;
  }
  var xKeys = Object.keys(x);
  var yKeys = Object.keys(y);
  for (var i = 0; i < xKeys.length; i++){
    if (!yKeys.includes(xKeys[i]) || !deepEqual(x[xKeys[i]], y[xKeys[i]])) {
      return false;
    }
  }
  return true;
}

