////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  if (num1 < num2){
    return num1;
  } else if (num1 > num2){
    return num2;
  } else {
    return 0;
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(number) {
  if (number % 2 === 0){
    return true;
  } else if (number % 2 !== 0){
    return false;
  }

}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, character) {
  let counter = 0;
  for (i = 0; i < string.length; i++){
    if (character === string[i]){
      counter += 1;
    }
}
return counter;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  let counter = 0
  for (i = 0; i < string.length; i++){
    if (string[i] === "B"){
      counter += 1;
    }
  }
  return counter;
}

